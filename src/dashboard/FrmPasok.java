    public void tampilCombo(){
        try{
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM tb_pemasok");
            
            while(rs.next())
            {
               TxtPemasok.addItem(rs.getString("nama_pemasok"));
            }
        }catch(Exception e){
           e.printStackTrace();
        }
        
    }
    public void kode(){
        try{
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM tb_pasok order by kd_pasok DESC");
            if(rs.next()){
                String kd = rs.getString("kd_pasok").substring(1);
                String kode = "000" + (Integer.parseInt(kd)+1);
                TxtKdPasok.setText("P"+kode);
            }else{
                TxtKdPasok.setText("P0001");
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Koneksi gagal"+e.getMessage());
        }
    }
