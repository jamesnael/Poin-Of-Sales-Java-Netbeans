    private void TxtPencarianKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtPencarianKeyReleased
        // TODO add your handling code here:
        try{
            st = cn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("Select * from tb_pasok where kd_barang like '%"+ TxtPencarian.getText() +"%'"
                    + " or nama_barang like '%"+ TxtPencarian.getText() +"%' "
                    + "or kd_pasok like '%"+ TxtPencarian.getText() +"%' "
                    + "or pemasok like '%"+ TxtPencarian.getText() +"%' "
                    + "or tgl_masuk like '%"+ TxtPencarian.getText() +"%'");
            while(rs.next()){
                Object[] data = {
                    rs.getString("kd_pasok"),
                    rs.getString("kd_barang"),
                    rs.getString("nama_barang"),
                    rs.getString("jumlah"),
                    rs.getString("tgl_masuk"),
                    rs.getString("pemasok")    
                };
                tabModel.addRow(data);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_TxtPencarianKeyReleased

