    private void TxtPemasokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TxtPemasokActionPerformed
        // TODO add your handling code here:   
        try{
            rs = st.executeQuery("SELECT * FROM tb_pemasok WHERE nama_pemasok = '"+TxtPemasok.getSelectedItem()+"'");
            if(rs.next()){          
                  TxtKdPemasok.setText(rs.getString("kd_pemasok"));             
            }else{
                TxtKdPemasok.setText("");
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Koneksi gagal"+e.getMessage());
        }
    }//GEN-LAST:event_TxtPemasokActionPerformed

