    private void TxtKdBarangKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtKdBarangKeyReleased
        // TODO add your handling code here:
        try{
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM tb_barang WHERE kd_barang = "
                    + "'"+TxtKdBarang.getText()+"'");
            if(rs.next()){
                if( TxtKdBarang.getText().equals(rs.getString("kd_barang"))){
                    TxtNmBarang.setText(rs.getString("nama_barang"));
                    TxtHarsat.setText(rs.getString("harga_jual"));
                }
            }else{
                TxtNmBarang.setText("");
                TxtHarsat.setText("");
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Koneksi gagal"+e.getMessage());
        }
    }//GEN-LAST:event_TxtKdBarangKeyReleased

