    public void kode(){
        try{
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM tb_transaksi "
                    + "order by kd_transaksi DESC");
            if(rs.next()){
                String kd = rs.getString("kd_transaksi").substring(1);
                String kode = "000" + (Integer.parseInt(kd)+1);
                TxtKdTransaksi.setText("T"+kode);
            }else{
                TxtKdTransaksi.setText("T0001");
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Koneksi gagal"+e.getMessage());
        }
    }

